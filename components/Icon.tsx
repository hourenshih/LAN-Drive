const getFileExtension = (fileName: string) => {
  return fileName.slice(((fileName.lastIndexOf(".") - 1) >>> 0) + 2).toLowerCase();
};

export const getIconTypeForFile = (fileName: string): IconProps['type'] => {
  const extension = getFileExtension(fileName);
  switch (extension) {
    case 'jpg':
    case 'jpeg':
    case 'png':
    case 'gif':
    case 'svg':
      return 'image';
    case 'doc':
    case 'docx':
    case 'pdf':
    case 'txt':
      return 'document';
    case 'zip':
    case 'rar':
    case '7z':
    case 'tar':
    case 'gz':
      return 'archive';
    default:
      return 'generic-file';
  }
};

